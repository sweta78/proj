                <h3 class="sub_heading"><strong>THE KOTWALS</strong></h3>
                <p class="profile_content_left"> <img src="image/healp-line-bullet.png" width="9" height="11" alt="history bullet"> Ranchi has a long history of policing through the famed institution of               the Kotwal. Malikul Umara Faqruddin is said to be the first Kotwal of               Ranchi. He became the Kotwal at the age of 40 in 1237 A.D. and was also               simultaneously appointed as the Naibe-Ghibat (Regent in absence). Because               of his integrity and sagacity he had a very long tenure, holding the post               through the reigions of three Sultans Balban, Kaikobad and Kaikhusrau.               On one occasion when some Turkish nobles had approached him to secure               the withdrawal of Balban's order. confiscating their estates, the Kotwal               is recorded to have said, "My words will carry no weight if I accept               any bribe from you. It is presumed that the Kotwal, or Police Head quarters               was then located at Qila Rai Pithora or today's Mehrauli. </p>
                <br>
                <p class="profile_content_left"> <img src="image/healp-line-bullet.png" width="9" height="11" alt="history bullet"> Another Kotwal mentioned in history books is Malik Alaul Mulk, who was                 appointed by Sultan Allauddin Khilji in 1297 AD. Sultan Alauddin Khilji                 once said of him, "He deserves the Wizarat (Prime Ministership)                 but I have appointed him only the Kotwal of Delhi on account of' his                 incapacitating corpulence." </p>
